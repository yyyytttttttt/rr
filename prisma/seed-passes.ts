async function main() {
  console.log('Seeding wellness passes...');

  for (const p of passes) {
    const { days, ...passData } = p;

    const existing = await prisma.wellnessPass.findUnique({ where: { slug: passData.slug } });

    if (existing) {
      console.log(`  ✓ "${passData.title}" already exists — skipping`);
      continue;
    }

    const created = await prisma.wellnessPass.create({
      data: {
        ...passData,
        days: {
          create: days,
        },
      },
    });

    console.log(`  + Created "${created.title}" (${days.length} days)`);
  }

  console.log('Done!');
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
